FAQ >> DES REPONSES A VOS QUESTIONS SUR LE RAMONAGE

Quelles sont les obligations légales autour du ramonage ?
La ramonage doit etre réalisé 2 fois par an par un ramoneur professionnel.
Ce n\'est pas un conseil, c\'est une obligation légale.

Quels risques encourus si on ne ramone pas un conduit ?
Les risques sont assez lourds et devraient vous motiver à ramoner vos conduits.
Voici les risques encourus:
- Le Feu de cheminée du à l\'embrasement des particules et suies des paroies est la menace qui vient en premier.
- L\' intoxication par la fumee ou par le monoxyde.

Enfin il y a 2 risques qui ne concernent pas la santé cette fois :
- votre responsabilité financiere est engagée en cas de dommage, les assurances ne vous couvriront pas
- votre responsabilité morale est engagé et vous risquez des poursuites légales.

En quoi consiste le ramonage d\'un conduit ?
La combustion du gaz ou du bois genere des suies et des particules qui se deposent sur les paroies. C\'est ce que le ramonage devra permettre d\'enlever. Cela consiste le plus souvent à passer un hérisson ( PVC ou metal) dans les conduits.
Attention à la récupération de la suie, cela peut être poussièreux !

Comment choisir un ramoneur ?
Nous vous donnons des conseils sur cette page http://www.alloramonage.fr/conseil_ramonage.php
mais vous pouvez commencer par comparer les offres des societes de ramonage pres de chez vous avec notre annuaire des ramoneurs http://www.alloramonage.fr/annuaire_ramoneur.php

Combien de temps dure un ramonage ?
- Pour une cheminée, une chaudiere à granule de bois ou un poele (voir Poelesabois.com), comptez 30 minutes,
- Le nettoyage d\'une Chaudiere prendra lui 45 minutes en moyenne
- Enfin l\'entretien annuel d\'une chaudière, et ce n\'est qu\'une moyenne la encore, durera de 1 heure a 1 heure 30.


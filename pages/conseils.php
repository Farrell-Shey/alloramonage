Comment savoir s'il faut ramoner plus souvent une cheminée ?
Dans certains cas il peut être nécessaire de ramoner plus souvent votre cheminée.

- Si vous constatez que la vitre noircit (cela peut venir de la qualité du combustible)
- Si vous constatez que la flamme change radicalement d'aspect ( après quelques semaines d'usage, sans autres raisons extérieures, elle devient soudain plus vive ou plus frêle en permanence)
vous pouvez être amené à ramoner la cheminée de façon encore plus fréquente.

Attention cet entretien trop régulier n'est pas normal. Demandez alors au ramoneur ce qui cloche, il y a sans doute un problème avec l'installation.

Comment ramoner et comment ramoner un poêle par le bas ?
Rappelons que le ramonage ne consiste pas qu'à nettoyer les conduits, il s'agit aussi de vérifier que l'installation est fonctionnelle et sure ! Et ce n'est pas une compétence qui s'improvise...

Cette précision importante étant faite, On peut ramoner de 2 façons :


Ramonage par le haut ou par le bas
- par le haut : c'est la technique traditionnelle. Il faut monter sur le toit quand c'est possible et passer le hérisson de la taille et de la matière adéquate (fonction des matériaux de votre conduit)

- par le bas : c'est moins fréquent mais tout aussi efficace. La technique dépendra des outils du ramoneurs mais aussi de votre habitation.

Ramonage mécanique ou rotatif
Traditionnellement le ramoneur fera un ramonage mécanique : Il frottera le hérisson tout le long du conduit pour faire tomber les particules et un peu de bistre.

De plus en plus de ramoneurs utilisent des techniques de ramonage rotatif qui vont très bien avec la technique du ramonage par le bas : Une intervention dans votre foyer rapide, efficace et propre (avec un système d'aspirateur)

Le mieux est de faire appel à une société de ramonage avant le début de la saison mais n'attendez pas le dernier moment...elles sont très prises quand vient l'hiver avec de nombreuses interventions à réaliser dans la journée.


Opérations pour un bon ramonage bois
1- Vérification de la vacuité du conduit

2- Brossage du conduit au hérisson pour un nettoyage complet de la paroi intérieur des conduits

3- Vérification de l’état du conduit

4- Contrôle du tirage (essai si nécessaire)


Opérations pour un bon ramonage gaz
1- Vérification de l’état du conduit

2- Contrôle du tirage

3- Vérification de la vacuité du conduit

Choix d'un ramoneur Professionnel
- D'abord le ramoneur doit être diplômé (D'après l'article 16 de la loi 96-603 du 5 Juillet 96 et lé décret , l'artisan doit avoir obtenu un certificat de qualification professionnelle de ramoneur fumiste ou a un grand nombre d'années d'expériences), sans ce diplôme l'assurance peut refuser d'activer l'assurance responsabilité civile en cas d'incident. Ceci étant il faut avouer que trop peu de professionnels sont diplômés (sauf en Alsace).

- Il faut bien sur vérifier le prix de la prestation ( déplacement compris?) et définir quels services sont compris (notamment pour une chaudière gaz). Parfois le professionnel propose un abonnement spécifique en cas d'entretien régulier (comme pour le gaz).

- Si vous avez une installation particulière qui peut nécessiter des outils spécifiques, pensez à demander s'ils disposent de tout l'équipement nécessaire avant son intervention.

- Enfin il devra être à même de vous fournir un certificat (attention les carnets de certificat de ramonage sont en vente libre). Vérifiez bien les mentions de ce certificat. Elles doivent engager l'artisan à vous couvrir via une assurance responsabilité civile en cas de pépin.

En cas de souci, l'assurance vous demandera un certificat de ramonage... et il faut garder a l'esprit que le ramonage est aussi une occasion de mieux régler votre installation qu'il s'agisse d'un poele, d'une cheminée ou d'une chaudière, source d'économie d'énergie.


RECHERCHEZ UN RAMONEUR PRES DE CHEZ VOUS
Respect des normes
- D'abord il convient d'utiliser un appareil de chauffage aux normes (respect de la règlementation en vigueur) avec un bon entretien. Evitez les vieux appareils non vérifiés, c'est idéal pour déclencher un incendie et libérer dans les airs des particules fines

- Les conduits de fumée ne doivent pas faire des allers et retours. 2 coudes grand maximum.

- Veillez a ce qu'ils soient bien isolés (gainage) avec respect des distances de sécurité.
Pour plus de détails voir les rubriques installation d'un poêle à bois ou installation d'un poêle à granulés du site Poeles.net qui sont riches d'enseignement.

- Veillez aussi a ce que la fumée s'évacue correctement (avec une sortie plus haute que le faitage du toit de votre foyer pour le bois) (voir les rubriques "sortie des conduits de fumée") de ce même site partenaire ou encore sur Cheminee.net en fonction de votre installation).

- Attention : les bûches dites "ramoneuses" (et le ramonage chimique) ne permettent pas l'économie d'un ramonage !
Elles permettent de sécher la suie ce qui peut aider à la faire tomber...ou pas. Ce ne sont donc pas des produits miracles.
Choix des combustibles
Le problématique du choix du combustible est essentielle pour le bois énergie. Choisissez un bois bien sec a haut pouvoir calorifique. Un produit sec, c'est moins de 20% d'humidité, soit bien souvent 2 ans de séchage. Les mélanges chêne charme hêtre sont souvent recommandés. Renseignez vous sur la technique d'allumage du top down. Evitez les bois verts, les bois de récupération (traités), les résineux.

Plus votre combustible sera de qualité, plus vous aurez de calories utiles pour chauffer la maison (meilleur rendement) et moins le ramoneur aura de particules à enlever du conduit !

